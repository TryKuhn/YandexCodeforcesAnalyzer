"""Prompt for a partial solution targeting a single subtask."""
from .base import ASCII_CODE_RULE, NO_FENCES

_TAG_HINT = {
    "TL": "правильное по логике, но НАМЕРЕННО слишком медленное для старших "
          "подзадач (используй простой алгоритм, например O(2^n) / O(n^2)). "
          "ЗАПРЕЩЕНО искусственное замедление (sleep/busy-loop).",
    "WA": "корректное только при ограничениях этой подзадачи; на старших "
          "подзадачах оно даёт неверный ответ из-за упрощающего предположения.",
    "ML": "правильное по логике, но с избыточной памятью на старших подзадачах.",
    "RE": "правильное при ограничениях подзадачи, но падает (RE) на старших.",
    "RJ": "решает только эту подзадачу, на старших отклоняется.",
}


def build_system_prompt(group: str, constraints: str, strategy: str, tag: str) -> str:
    """Build the system prompt for a partial solution targeting one subtask."""
    hint = _TAG_HINT.get(tag, _TAG_HINT["WA"])
    return (
        "Ты — эксперт по разработке задач для Polygon. "
        f"Напиши решение на C++, которое КОРРЕКТНО решает ТОЛЬКО подзадачу {group} "
        f"(ограничения: {constraints}). "
        f"Используй более простой подход: {strategy}. "
        f"На полном наборе тестов это решение должно получать вердикт {tag}: {hint} "
        "Решение должно компилироваться и читать тот же формат ввода/вывода, что и "
        "основное решение.\n"
        f"{ASCII_CODE_RULE}\n{NO_FENCES}"
    )
