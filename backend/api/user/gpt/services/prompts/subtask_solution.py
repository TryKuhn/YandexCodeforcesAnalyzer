"""Prompt for a partial solution targeting a single subtask."""
from .base import ASCII_CODE_RULE, NO_FENCES, SKIP_MARKER

_TAG_HINT = {
    "TL": "правильный по ответам, но НАСТОЯЩИЙ слишком медленный алгоритм для старших "
          "подзадач (например перебор перестановок O(n!) / подмножеств O(2^n) / O(n^2)). "
          "На малых входах даёт верный ответ, на больших не успевает. НИКАКИХ пустых/"
          "бесконечных циклов и искусственных задержек.",
    "WA": "правдоподобный, но неверный алгоритм: корректен при ограничениях этой "
          "подзадачи, а на старших даёт неверный ответ из-за упрощающего допущения. "
          "Вывод правильного ФОРМАТА, но неверный по значению (иначе PE вместо WA).",
    "ML": "правильный по ответам алгоритм, но с избыточной памятью по самой структуре "
          "(например O(n^2) памяти вместо O(n)); на старших подзадачах естественно "
          "превышает лимит. Без искусственного выделения памяти не по алгоритму.",
    "RE": "правильный при ограничениях подзадачи, но на старших ЕСТЕСТВЕННО падает "
          "(например переполнение стека рекурсией, выход за границу массива), а не через "
          "намеренный краш (assert(false)/abort запрещены).",
    "RJ": "решает только эту подзадачу, на старших отклоняется.",
}


def build_system_prompt(group: str, constraints: str, strategy: str, tag: str) -> str:
    """Build the system prompt for a partial solution targeting one subtask."""
    hint = _TAG_HINT.get(tag, _TAG_HINT["WA"])
    return (
        "Ты — эксперт по разработке задач для Polygon. "
        f"Напиши решение на C++, которое КОРРЕКТНО решает подзадачу {group} "
        f"(ограничения: {constraints}) настоящим алгоритмом. "
        f"Подход: {strategy}. "
        f"На полном наборе тестов оно должно получать РОВНО вердикт {tag}: {hint} "
        "Решение должно компилироваться и читать тот же формат ввода/вывода, что и "
        "основное решение.\n"
        f"Если ты НЕ можешь честно гарантировать вердикт {tag} настоящим алгоритмом для "
        f"этой задачи — не выдумывай. Верни РОВНО одну строку `{SKIP_MARKER}: <краткая "
        "причина>` вместо кода.\n"
        f"{ASCII_CODE_RULE}\n{NO_FENCES}"
    )
